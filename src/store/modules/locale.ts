import { defineStore } from "pinia";

export type Locale = "en" | "zh";

export const useLocale = defineStore("locale", {
  state: (): { locale: Locale } => ({
    locale: "en"
  }),
  actions: {
    toggleLocale() {
      this.locale = this.locale === "en" ? "zh" : "en";
    },
    setLocale(locale: Locale) {
      this.locale = locale;
    }
  },
  persist: true
});

